# 字符串
str = "Hello world!"
print(len(str))

# 通过索引获取单个字符
print(str[0])
print(str[11])

# 布尔类型 必须大写开头
b1 = True
b2 = False

# 空值类型
n = None

# 数字类型
num1 = 1
num2 = 1.5
num3 = 1.50

# 输出上述字符的类型
print(type(str))
print(type(b1))
print(type(b2))
print(type(n))
print(type(num1))
print(type(num2))
print(type(num3))

